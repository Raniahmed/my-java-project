package dataTypes;

public class Booleans {

    public static void main(String[] args) {
        // booleans represent true or false
        boolean b = true;
        boolean c = false;

        boolean r = 10 > 9;
        boolean res = 20 > 25;

        System.out.println(b);
        System.out.println(c);
        System.out.println(r);
        System.out.println(res);
    }
}
