package chapter3;

// Char Variables behave like integers,

public class CharDemo2 {
    public static void main(String[] args) {
        char ch1;
        ch1 = 'A'; // 65
        System.out.println("ch1 contains " + ch1);
        ch1++; //increment ch1
        System.out.println("ch1 is now " + ch1);
    }
}
